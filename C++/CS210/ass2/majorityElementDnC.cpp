void merge (vector<int>& arr, int left, int mid, int right) {
    int n1 = mid - left + 1;
    int n2 = right - mid;
    vector<int> L(n1), R(n2);

    for (int i = 0; i < n1; i++)
        L[i] = arr[left + i];
    for (int j = 0; j < n2; j++)
        R[j] = arr[mid + 1 + j];

    int i = 0, j = 0;
    int k = left;
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {
            arr[k] = L[i];
            i++;
        }
        else {
            arr[k] = R[j];
            j++;
        }
        k++;
    }
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }
}

void mergeSort (vector<int>& arr, int left, int right) {
    if (left >= right)
        return;
    int mid = left + (right - left) / 2;
    mergeSort(arr, left, mid);
    mergeSort(arr, mid + 1, right);
    merge(arr, left, mid, right);
}

// Boyer - Moore voting algorithm
int majorityele(vector<int> a){
    int i=0,cnt=0;
    int ele;
    for(i=0;i<a.size();i++){
        if(cnt==0){
            cnt=1;
            ele=a[i];}
        else if(a[i]==ele) cnt++;
        else cnt--;
    }
    int cnt_1=0;
    for(i=0;i<a.size();i++){
        if(a[i]==ele)
            cnt_1++;
        }
    if(cnt_1>(a.size()/2)) return ele;
    return -1;
}

